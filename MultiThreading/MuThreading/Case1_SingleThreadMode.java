package MuThreading;


public class Case1_SingleThreadMode {

    // Whever Execution starts from Main Method one thread is started automatically called as 'Main Thread'
    public static void main(String[] args) {    //Main Thread Started/
        System.out.println("Hello world..!");
        System.out.println("Hello world..!");
        System.out.println("Hello world..!");      // Main Thread Executing
        System.out.println("Hello world..!");
    }   // Main Thread Ended.

}
